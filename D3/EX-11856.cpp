
#include<iostream>
#include<cstring>

using namespace std;

int main(int argc, char** argv)
{
	int test_case;
	int T;

	cin >> T;
	/*
	   여러 개의 테스트 케이스가 주어지므로, 각각을 처리합니다.
	*/
	for (test_case = 1; test_case <= T; ++test_case)
	{
		string in;
		string out = "Yes";

		cin >> in;

		int tmp[26] = { 0 };

		for (int i = 0; i < 4; i++)
			tmp[in[i] - 'A']++;

		for(int i = 0; i < 26; i++)
			if ((tmp[i] != 0))
			{
				if (tmp[i] != 2)
				{
					out = "No";
					break;
				}
			}

		printf("#%d ", test_case);
		cout << out << '\n';
	}
	return 0;//정상종료시 반드시 0을 리턴해야합니다.
}